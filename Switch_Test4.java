class Switch_Test4 {
	public static void main(String ar[]) {
		String str = "D";
		switch (str) {
			case "A" : System.out.println('A');
					break;
			case "B" : System.out.println('B');
					break;
			case "C" : System.out.println('C');
					break;
			case "D" : System.out.println('D');
					break;
			default : System.out.println("잘못입력");
		}
	}
}